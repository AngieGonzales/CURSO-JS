// loops o bucles

// For (se ejecuta siempre y cuando la condicion sea true cuando sea false deja de ejecutarse)
for (let i = 0; i < 5; i++) {
    console.log(`Hola ${i}`)
}


const numbers = [1,2,3,4,5,6]

for (let i = 0; i < numbers.length; i++){
    console.log(`Elemento: ${numbers[i]}`)
}

